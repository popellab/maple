    @model_validator(mode="after")
    def validate_large_variance_documented(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Check if large variance is documented in limitations.

        CV > 50% suggests substantial variability that should be explained.
        Only applies to scalar inputs (vector inputs are checked per-element elsewhere).
        """
        import warnings

        # Find mean and SD/SE inputs (only scalar inputs)
        mean_input = None
        std_input = None

        for inp in self.calibration_target_estimates.inputs:
            # Skip vector-valued inputs for this check
            if isinstance(inp.value, list):
                continue

            name_lower = inp.name.lower()
            if "mean" in name_lower and mean_input is None:
                mean_input = inp
            if any(x in name_lower for x in ["sd", "std", "se", "stderr", "stdev"]):
                std_input = inp

        if mean_input and std_input:
            mean_val = mean_input.value
            std_val = std_input.value

            # Both must be scalar at this point (we skipped lists above)
            if not isinstance(mean_val, list) and not isinstance(std_val, list) and mean_val != 0:
                # Calculate CV (coefficient of variation)
                cv = abs(std_val / mean_val)

                if cv > 0.5:  # CV > 50%
                    # Check if caveats or rationale mention variance/uncertainty
                    caveats_text = " ".join(self.caveats).lower()
                    rationale_lower = self.rationale.lower()
                    combined_text = caveats_text + " " + rationale_lower
                    variance_keywords = [
                        "variance",
                        "variability",
                        "uncertain",
                        "cv",
                        "wide",
                        "heterogen",
                        "variation",
                        "spread",
                        "dispersion",
                    ]

                    if not any(keyword in combined_text for keyword in variance_keywords):
                        warnings.warn(
                            f"Large coefficient of variation (CV = {cv:.1%}) detected but not discussed in caveats. "
                            f"Consider adding a caveat explaining whether this reflects biological variability, "
                            f"measurement error, or heterogeneous population.",
                            UserWarning,
                        )

        return self

