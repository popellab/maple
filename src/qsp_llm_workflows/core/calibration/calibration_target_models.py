class CalibrationTargetFooters(BaseModel):
    """
    Footer fields for calibration target files.

    These are metadata about the file itself (not generated by LLM):
    - What observable is being tracked
    - Which cancer type
    - When and how it was derived
    """

    calibration_target_id: str = Field(description="Unique calibration target identifier")
    cancer_type: str = Field(description="Cancer type (e.g., 'PDAC')")
    tags: List[str] = Field(default_factory=list, description="Metadata tags")
    derivation_id: Optional[str] = Field(None, description="Unique derivation identifier")
    derivation_timestamp: Optional[str] = Field(None, description="ISO timestamp of derivation")


class IndexType(str, Enum):
    """Type of index dimension for vector-valued calibration targets."""

    TIME = "time"  # Time-course data (e.g., measurements at Day 0, 7, 14)
    DOSE = "dose"  # Dose-response data (e.g., drug concentrations)
    RATIO = "ratio"  # Ratio-based index (e.g., E:T ratios)
    CONCENTRATION = "concentration"  # Concentration series
    OTHER = "other"  # Other index types


class CalibrationTargetEstimates(BaseModel):
    """
    Calibration target estimates with structured inputs and derivation.

    Supports both scalar and vector-valued outputs:
    - Scalar: Single measurement → length-1 lists (median=[42.0], ci95=[[37.0, 47.0]])
    - Vector: Multi-point data → lists matching index_values length

    Vector-valued outputs are indexed by index_values (e.g., time points, doses).
    """

    median: List[float] = Field(
        description=(
            "COMPUTED median value(s) from distribution_code. "
            "Length-1 list for scalar data, or list matching index_values length for vector data."
        )
    )
    ci95: List[List[float]] = Field(
        description=(
            "COMPUTED 95% confidence intervals from distribution_code. "
            "List of [lower, upper] pairs. Length-1 for scalar data ([[lo, hi]]), "
            "or matching index_values length for vector data."
        )
    )
    units: str = Field(description="Units of the observable (Pint-parseable)")

    # Sample size metadata
    sample_size: Union[int, List[int]] = Field(
        description=(
            "Sample size (n) for the measurement. Critical for uncertainty quantification "
            "and pooling across studies. Single int for scalar data or same n for all index "
            "points. List[int] for different sample sizes at each index point (must match "
            "index_values length)."
        )
    )
    sample_size_rationale: str = Field(
        description=(
            "Explanation of how sample_size was determined. Document the source "
            "(e.g., 'n=5 per group stated in Methods section', 'n inferred from "
            "figure error bars using SEM formula', 'n estimated as typical for this study type')."
        )
    )

    # Index dimension for vector-valued data
    index_values: Optional[List[float]] = Field(
        None,
        description=(
            "Index values for vector-valued data (e.g., [0, 24, 48, 72] for time points). "
            "None for scalar data. Vector-valued inputs and outputs must match this length."
        ),
    )
    index_unit: Optional[str] = Field(
        None,
        description="Units for index_values (e.g., 'hour', 'day', 'nanomolar'). Required if index_values provided.",
    )
    index_type: Optional[IndexType] = Field(
        None,
        description="Type of index dimension. Required if index_values provided.",
    )

    inputs: List[EstimateInput] = Field(
        description=(
            "List of literature-extracted inputs used in distribution_code derivation. "
            "These are the VALUES REPORTED IN THE PAPER (mean, SD, median, IQR, trajectories, etc.) "
            "that are used to DERIVE the distribution via Monte Carlo. "
            "Inputs can be scalar (broadcast to all index points) or vector-valued (same length as index_values).\n\n"
            "NOTE: For inputs used in submodel or observable code, use submodel.inputs or observable.inputs instead."
        )
    )
    assumptions: List[ModelingAssumption] = Field(
        default_factory=list,
        description=(
            "List of modeling assumptions used in derivation. "
            "These are values NOT from the paper but needed for computation (e.g., n_mc_samples, "
            "assumed_cv when not reported). Each must have a rationale field explaining the choice."
        ),
    )
    distribution_code: str = Field(
        description=(
            "Python code defining a derive_distribution(inputs, ureg) function.\n"
            "inputs is a dict mapping input names to Pint Quantities (scalar or array).\n"
            "Must return dict with Pint Quantities:\n"
            "  - median_obs: array of medians (length matching index_values, or length 1 for scalar)\n"
            "  - ci95_obs: list of [lower, upper] pairs (same length)\n\n"
            "For scalar data, return length-1 arrays.\n"
            "For vector data, return arrays matching index_values length.\n\n"
            "Example (scalar - single measurement):\n"
            "def derive_distribution(inputs, ureg):\n"
            "    import numpy as np\n"
            "    mean = inputs['cd8_density_mean'].magnitude\n"
            "    sd = inputs['cd8_density_sd'].magnitude\n"
            "    units = inputs['cd8_density_mean'].units\n"
            "    samples = np.random.normal(mean, sd, 10000)\n"
            "    return {\n"
            "        'median_obs': np.array([np.median(samples)]) * units,\n"
            "        'ci95_obs': [[np.percentile(samples, 2.5) * units, np.percentile(samples, 97.5) * units]]\n"
            "    }\n\n"
            "Example (vector - time-course data):\n"
            "def derive_distribution(inputs, ureg):\n"
            "    import numpy as np\n"
            "    means = inputs['cd8_density_mean'].magnitude  # array\n"
            "    sds = inputs['cd8_density_sd'].magnitude  # array\n"
            "    units = inputs['cd8_density_mean'].units\n"
            "    n_points = len(means)\n"
            "    medians, ci95s = [], []\n"
            "    for i in range(n_points):\n"
            "        samples = np.random.normal(means[i], sds[i], 10000)\n"
            "        medians.append(np.median(samples))\n"
            "        ci95s.append([np.percentile(samples, 2.5) * units, np.percentile(samples, 97.5) * units])\n"
            "    return {\n"
            "        'median_obs': np.array(medians) * units,\n"
            "        'ci95_obs': ci95s\n"
            "    }"
        )
    )

    @field_validator("sample_size")
    @classmethod
    def validate_sample_size_positive(cls, v: Union[int, List[int]]) -> Union[int, List[int]]:
        """Validate sample size is at least 1."""
        if isinstance(v, list):
            for i, n in enumerate(v):
                if n < 1:
                    raise ValueError(f"sample_size[{i}] must be at least 1, got {n}")
        else:
            if v < 1:
                raise ValueError(f"sample_size must be at least 1, got {v}")
        return v

    @model_validator(mode="after")
    def validate_output_lengths_match(self) -> "CalibrationTargetEstimates":
        """Validator: Ensure median and ci95 have the same length."""
        n_median = len(self.median)
        n_ci95 = len(self.ci95)

        if n_median != n_ci95:
            raise ValueError(
                f"Output array lengths must match: median has {n_median}, ci95 has {n_ci95}"
            )

        # Check ci95 inner structure
        for i, ci in enumerate(self.ci95):
            if len(ci) != 2:
                raise ValueError(f"ci95[{i}] must be [lower, upper] pair, got {len(ci)} elements")

        return self

    @model_validator(mode="after")
    def validate_index_fields_consistent(self) -> "CalibrationTargetEstimates":
        """Validator: Ensure index_* fields are all present or all absent."""
        has_values = self.index_values is not None
        has_unit = self.index_unit is not None
        has_type = self.index_type is not None

        if has_values:
            if not has_unit:
                raise ValueError("index_unit is required when index_values is provided")
            if not has_type:
                raise ValueError("index_type is required when index_values is provided")

            # Check that output length matches index_values length
            # Type narrowing: we know index_values is not None here
            assert self.index_values is not None  # for type checker
            n_index = len(self.index_values)
            n_output = len(self.median)
            if n_output != n_index:
                raise ValueError(
                    f"Output length ({n_output}) must match index_values length ({n_index})"
                )
        else:
            # Scalar case: must have length 1
            if len(self.median) != 1:
                raise ValueError(
                    f"For scalar data (no index_values), outputs must have length 1, "
                    f"got {len(self.median)}"
                )

        return self

    @model_validator(mode="after")
    def validate_vector_input_lengths(self) -> "CalibrationTargetEstimates":
        """Validator: Ensure vector-valued inputs and assumptions match index_values length."""
        if self.index_values is None:
            return self  # Scalar case, skip

        n_index = len(self.index_values)

        # Check literature inputs
        for inp in self.inputs:
            if isinstance(inp.value, list):
                if len(inp.value) != n_index:
                    raise ValueError(
                        f"Vector input '{inp.name}' has length {len(inp.value)}, "
                        f"but index_values has length {n_index}. "
                        f"Vector inputs must match index_values length."
                    )

        # Check assumptions
        for assumption in self.assumptions:
            if isinstance(assumption.value, list):
                if len(assumption.value) != n_index:
                    raise ValueError(
                        f"Vector assumption '{assumption.name}' has length {len(assumption.value)}, "
                        f"but index_values has length {n_index}. "
                        f"Vector assumptions must match index_values length."
                    )

        return self

    @model_validator(mode="after")
    def validate_sample_size_length(self) -> "CalibrationTargetEstimates":
        """Validator: Ensure vector sample_size matches index_values length."""
        if isinstance(self.sample_size, list):
            if self.index_values is None:
                raise ValueError(
                    "Vector sample_size requires index_values. " "Use a single int for scalar data."
                )
            if len(self.sample_size) != len(self.index_values):
                raise ValueError(
                    f"sample_size length ({len(self.sample_size)}) must match "
                    f"index_values length ({len(self.index_values)})"
                )
        return self


class CalibrationTarget(BaseModel):
    """
    A calibration target: a raw observable extracted from literature.

    Used to calibrate QSP model parameters via Bayesian inference.

    Excludes header fields that are added during unpacking:
    - calibration_target_id, cancer_type
    - tags, derivation_id, derivation_timestamp
    """

    # --- Observable fields (LLM-generated) ---
    calibration_target_estimates: CalibrationTargetEstimates = Field(
        description="Observable estimates with inputs and derivation"
    )

    # ========================================================================
    # Field definitions
    # ========================================================================

    # --- Observable specification (LLM-generated) ---
    observable: Observable = Field(
        description=(
            "How to compute the experimental observable from full model species. "
            "Defines the code that transforms model output to match literature measurements."
        )
    )

    # --- Experimental context (LLM-generated, grouped for distance calculations) ---
    experimental_context: ExperimentalContext = Field(
        description="Experimental context of the observable"
    )

    # --- Scenario (optional, for intervention tracking) ---
    scenario: Optional[Scenario] = Field(
        default=None,
        description=(
            "Experimental scenario with interventions. Optional - can be omitted if "
            "no interventions or if intervention details are captured in rationale."
        ),
    )

    # --- Consolidated narrative (LLM-generated) ---
    rationale: str = Field(
        description=(
            "Single consolidated explanation covering:\n"
            "- What observable is being measured and why it's relevant\n"
            "- How the experimental data maps to model species/parameters\n"
            "- Key assumptions made in the derivation\n"
            "- Any interventions or experimental conditions\n\n"
            "This replaces: description, study_overview, study_design, "
            "derivation_explanation, scenario.description, and key_assumptions."
        )
    )

    caveats: List[str] = Field(
        default_factory=list,
        description=(
            "List of limitations, assumptions, or caveats as simple strings.\n"
            "Examples:\n"
            "- 'Values estimated from figures, not tabulated'\n"
            "- 'Single-center cohort (n~15), limited generalizability'\n"
            "- 'Bulk CD3+ assay, not CD8-specific'\n"
            "- 'Normal distribution assumed for positive-only data'"
        ),
    )

    # --- Sources (LLM-generated) ---
    primary_data_source: Source = Field(
        description="Primary data source (the paper where this observable was measured)"
    )
    secondary_data_sources: List[SecondarySource] = Field(
        default_factory=list, description="Secondary data sources (reference values, constants)"
    )

    @classmethod
    def get_header_fields(cls) -> set[str]:
        """Get set of field names that are headers (from CalibrationTargetFooters)."""
        return set(CalibrationTargetFooters.model_fields.keys())

    def split(self) -> tuple[CalibrationTargetFooters, dict]:
        """
        Split into headers and content.

        Returns:
            Tuple of (headers_model, content_dict)
        """
        header_fields = self.get_header_fields()
        all_data = self.model_dump()

        # Extract headers
        header_data = {k: v for k, v in all_data.items() if k in header_fields}
        headers = CalibrationTargetFooters(**header_data)

        # Extract content
        content = {k: v for k, v in all_data.items() if k not in header_fields}

        return headers, content

    @classmethod
    def from_split(cls, headers: CalibrationTargetFooters, content: dict) -> "CalibrationTarget":
        """
        Create CalibrationTarget from headers and content.

        Args:
            headers: CalibrationTargetFooters instance
            content: Content dictionary (LLM-generated fields)

        Returns:
            Complete CalibrationTarget instance
        """
        return cls(**{**headers.model_dump(), **content})

    @model_validator(mode="after")
    def validate_doi_resolution(self) -> "CalibrationTarget":
        """Validator: Check that primary DOI resolves via CrossRef."""
        if self.primary_data_source.doi:
            metadata = resolve_doi(self.primary_data_source.doi)
            if metadata is None:
                raise DOIResolutionError(
                    f"DOI '{self.primary_data_source.doi}' failed to resolve via CrossRef. "
                    "Verify the DOI exists and is correctly formatted (e.g., '10.1234/journal.2023.123'). "
                    "Search for the paper on Google Scholar or PubMed to find the correct DOI."
                )
        return self

    @model_validator(mode="after")
    def validate_title_match(self) -> "CalibrationTarget":
        """Validator: Check that paper title matches CrossRef metadata."""
        if self.primary_data_source.doi:
            metadata = resolve_doi(self.primary_data_source.doi)
            if metadata:
                crossref_title = metadata.get("title", "")
                if not fuzzy_match(crossref_title, self.primary_data_source.title, threshold=0.75):
                    raise PaperTitleMismatchError(
                        f"Paper title mismatch:\n"
                        f"  CrossRef: '{crossref_title}'\n"
                        f"  Provided: '{self.primary_data_source.title}'\n"
                        f"Use the exact title from the DOI. Copy the title from CrossRef or the paper itself."
                    )
        return self

    @model_validator(mode="after")
    def validate_secondary_doi_resolution(self) -> "CalibrationTarget":
        """Validator: Check that secondary source DOIs resolve via CrossRef.

        Secondary sources use doi_or_url field which can be a DOI or URL.
        Only validates entries that look like DOIs (start with '10.').
        """
        for source in self.secondary_data_sources:
            doi_or_url = source.doi_or_url
            if not doi_or_url:
                continue

            # Check if it looks like a DOI (starts with 10. or contains doi.org)
            is_doi = doi_or_url.startswith("10.") or "doi.org/10." in doi_or_url

            if is_doi:
                metadata = resolve_doi(doi_or_url)
                if metadata is None:
                    raise DOIResolutionError(
                        f"Secondary source '{source.source_tag}' has DOI '{doi_or_url}' "
                        f"that failed to resolve via CrossRef.\n"
                        f"Verify the DOI exists and is correctly formatted (e.g., '10.1234/journal.2023.123').\n"
                        f"If this is a URL (not a DOI), ensure it doesn't start with '10.' or contain 'doi.org'."
                    )
        return self

    @model_validator(mode="after")
    def validate_secondary_title_match(self) -> "CalibrationTarget":
        """Validator: Check that secondary source titles match CrossRef metadata.

        Only validates entries with DOIs (not URLs).
        """
        for source in self.secondary_data_sources:
            doi_or_url = source.doi_or_url
            if not doi_or_url:
                continue

            # Check if it looks like a DOI
            is_doi = doi_or_url.startswith("10.") or "doi.org/10." in doi_or_url

            if is_doi:
                metadata = resolve_doi(doi_or_url)
                if metadata:
                    crossref_title = metadata.get("title", "")
                    if not fuzzy_match(crossref_title, source.title, threshold=0.75):
                        raise PaperTitleMismatchError(
                            f"Secondary source '{source.source_tag}' title mismatch:\n"
                            f"  CrossRef: '{crossref_title}'\n"
                            f"  Provided: '{source.title}'\n"
                            f"Use the exact title from the DOI. Copy the title from CrossRef or the paper itself."
                        )
        return self

    @model_validator(mode="after")
    def validate_observable_code_units(self, info: ValidationInfo) -> "CalibrationTarget":
        """
        Validator: Check that observable code executes and returns correct units.

        Requires context:
            species_units: Dict mapping species names to unit strings (from species_units.json)
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        from qsp_llm_workflows.core.unit_registry import ureg
        from qsp_llm_workflows.core.calibration.code_validator import (
            CodeType,
            validate_code_block,
        )
        import pint

        # Get species_units from context
        if not info.context:
            raise ValueError(
                "Validation context is required. Pass context={'species_units': {...}}"
            )
        species_units = info.context["species_units"]

        # Use CodeValidator for syntax and signature checks
        result = validate_code_block(
            self.observable.code,
            CodeType.OBSERVABLE,
            check_hardcoded=False,  # Handled by separate validator
            check_execution=False,  # We do custom execution below
        )

        if not result.passed:
            errors = result.get_errors()
            if errors:
                first_error = errors[0]
                if first_error.category == "syntax":
                    raise CodeSyntaxError(
                        f"observable.code has syntax error: {first_error.message}"
                    )
                elif first_error.category == "signature":
                    raise CodeStructureError(first_error.message)
                else:
                    raise CodeStructureError(first_error.message)

        # Execute with mock data
        try:
            # Create mock time array
            mock_time = np.linspace(0, 14, 100) * ureg.day

            # Create mock species from actual model (with matching length)
            mock_species = create_mock_species(species_units, ureg, n_timepoints=len(mock_time))

            # Build constants dict from observable.constants
            constants = {}
            for const in self.observable.constants:
                constants[const.name] = const.value * ureg(const.units)

            # Execute function
            local_scope = {"ureg": ureg, "np": np}
            exec(self.observable.code, local_scope)
            compute_fn = local_scope["compute_observable"]

            result = compute_fn(mock_time, mock_species, constants, ureg)

            # Check result has units
            if not hasattr(result, "units"):
                raise MissingUnitsError("Function must return a Pint Quantity with units")

            # Check dimensionality matches calibration target units
            expected_quantity = 1.0 * ureg(self.calibration_target_estimates.units)
            if result.dimensionality != expected_quantity.dimensionality:
                raise DimensionalityMismatchError(
                    f"Observable code unit dimensionality mismatch:\n"
                    f"  Expected: {self.calibration_target_estimates.units} ({expected_quantity.dimensionality})\n"
                    f"  Got: {result.units} ({result.dimensionality})"
                )

            # Check result has same length as time series (no time indexing)
            if hasattr(result, "magnitude"):
                result_magnitude = result.magnitude
                # Handle both scalar and array results
                if np.isscalar(result_magnitude):
                    raise ScalarReturnError(
                        f"Observable code returned a scalar, but should return an array with same length as time series.\n"
                        f"  Expected length: {len(mock_time)}\n"
                        f"  Got: scalar value\n"
                        f"Do NOT use time indexing (e.g., species[-1] or species[0]) in observable code.\n"
                        f"Compute the observable over the entire time series."
                    )
                elif len(result_magnitude) != len(mock_time):
                    raise ArrayLengthError(
                        f"Observable code returned array with wrong length:\n"
                        f"  Expected: {len(mock_time)} (same as time series)\n"
                        f"  Got: {len(result_magnitude)}\n"
                        f"Do NOT use time indexing. Compute over entire time series."
                    )

        except (
            pint.DimensionalityError,
            pint.UndefinedUnitError,
            pint.OffsetUnitCalculusError,
        ) as e:
            # Wrap Pint unit errors in our custom exception
            raise UnitConversionError(
                f"Observable code has unit error: {str(e)}\n"
                f"Check that all unit operations are dimensionally consistent."
            ) from e
        except CalibrationTargetValidationError:
            # Re-raise all our custom validation errors
            raise
        except Exception:
            # Other errors might be from complex logic or missing species
            # Be lenient - actual execution will catch these
            pass

        return self

    @model_validator(mode="after")
    def validate_input_values_in_snippets(self) -> "CalibrationTarget":
        """Validator: Check that input values appear in their value_snippet.

        Ensures data integrity by verifying that extracted values can be found
        in the source text. Handles multiple numeric formats (decimal, scientific,
        percentage, rounded variations).

        Checks inputs in:
        - calibration_target_estimates.inputs (EstimateInput)
        - observable.inputs (SubmodelInput)

        Raises SnippetValueMismatchError if a value is not found in its snippet.
        """

        def check_inputs(inputs, location_prefix: str):
            """Helper to check value_snippet for a list of inputs."""
            for inp in inputs:
                value = inp.value
                snippet = inp.value_snippet

                if not snippet:
                    continue

                # Handle both scalar and vector-valued inputs
                values_to_check = [value] if not isinstance(value, list) else value

                missing_values = []
                for val in values_to_check:
                    if not check_value_in_text(snippet, val):
                        missing_values.append(val)

                if missing_values:
                    raise SnippetValueMismatchError(
                        f"{location_prefix} input '{inp.name}' has value(s) not found in value_snippet.\n"
                        f"  Missing values: {missing_values}\n"
                        f"  Snippet: '{snippet[:200]}{'...' if len(snippet) > 200 else ''}'\n"
                        f"The value_snippet must contain the exact values extracted from the paper.\n"
                        f"Check for:\n"
                        f"  - Typos in the extracted value\n"
                        f"  - Different numeric format in the paper (e.g., '2.5×10⁻³' vs '0.0025')\n"
                        f"  - Value reported in different units that need conversion\n"
                        f"  - Snippet truncated before the value appears"
                    )

        # Check calibration_target_estimates.inputs
        check_inputs(self.calibration_target_estimates.inputs, "calibration_target_estimates")

        # Check observable.inputs (if present and not IsolatedSystemTarget)
        if type(self).__name__ != "IsolatedSystemTarget" and self.observable.inputs:
            check_inputs(self.observable.inputs, "observable")

        return self

    @model_validator(mode="after")
    def validate_derivation_code(self) -> "CalibrationTarget":
        """
        Validator: Check that derivation_code executes, returns correct structure and units,
        and computed values match reported values (element-wise for vector data).
        """
        from qsp_llm_workflows.core.unit_registry import ureg
        from qsp_llm_workflows.core.calibration.code_validator import (
            CodeType,
            validate_code_block,
        )

        # Use CodeValidator for syntax and signature checks
        result = validate_code_block(
            self.calibration_target_estimates.distribution_code,
            CodeType.DISTRIBUTION,
            check_hardcoded=True,  # Check for hardcoded constants with units
            check_execution=False,  # We do custom execution below
        )

        if not result.passed:
            errors = result.get_errors()
            if errors:
                first_error = errors[0]
                if first_error.category == "syntax":
                    raise CodeSyntaxError(
                        f"distribution_code has syntax error: {first_error.message}"
                    )
                elif first_error.category == "signature":
                    raise CodeStructureError(first_error.message)
                else:
                    raise CodeStructureError(first_error.message)

        # Execute with mock inputs to test structure and units
        try:
            # Create mock inputs dict with Pint quantities
            # Merge both literature inputs and modeling assumptions into a single dict
            mock_inputs = {}
            # Add literature inputs
            for inp in self.calibration_target_estimates.inputs:
                if isinstance(inp.value, list):
                    mock_inputs[inp.name] = np.array(inp.value) * ureg(inp.units)
                else:
                    mock_inputs[inp.name] = inp.value * ureg(inp.units)
            # Add modeling assumptions
            for assumption in self.calibration_target_estimates.assumptions:
                if isinstance(assumption.value, list):
                    mock_inputs[assumption.name] = np.array(assumption.value) * ureg(
                        assumption.units
                    )
                else:
                    mock_inputs[assumption.name] = assumption.value * ureg(assumption.units)

            # Execute function
            local_scope = {"ureg": ureg, "np": np}
            exec(self.calibration_target_estimates.distribution_code, local_scope)
            derive_fn = local_scope["derive_distribution"]

            result = derive_fn(mock_inputs, ureg)

            # Check result is dict with required keys
            if not isinstance(result, dict):
                raise ReturnStructureError("Function must return a dict")

            required_keys = {"median_obs", "ci95_obs"}
            if not required_keys.issubset(result.keys()):
                raise ReturnStructureError(
                    f"Function must return dict with keys: {required_keys}. Got: {result.keys()}"
                )

            # Check median_obs has units
            if not hasattr(result["median_obs"], "units"):
                raise MissingUnitsError("Result['median_obs'] must be a Pint Quantity with units")

            # Check dimensionality matches expected units
            expected_quantity = 1.0 * ureg(self.calibration_target_estimates.units)

            # Check median
            if result["median_obs"].dimensionality != expected_quantity.dimensionality:
                raise DimensionalityMismatchError(
                    f"median_obs unit dimensionality mismatch:\n"
                    f"  Expected: {self.calibration_target_estimates.units} ({expected_quantity.dimensionality})\n"
                    f"  Got: {result['median_obs'].units} ({result['median_obs'].dimensionality})"
                )

            # Check ci95_obs is list of [lower, upper] pairs matching expected length
            n_expected = len(self.calibration_target_estimates.median)
            ci95_result = result["ci95_obs"]

            if not hasattr(ci95_result, "__len__") or len(ci95_result) != n_expected:
                raise ReturnStructureError(
                    f"ci95_obs must be a list of {n_expected} [lower, upper] pairs, "
                    f"got {len(ci95_result) if hasattr(ci95_result, '__len__') else 'non-list'}"
                )

            # Check each ci95 pair
            for i, ci_pair in enumerate(ci95_result):
                if not hasattr(ci_pair, "__len__") or len(ci_pair) != 2:
                    raise ReturnStructureError(
                        f"ci95_obs[{i}] must be [lower, upper] pair, "
                        f"got {len(ci_pair) if hasattr(ci_pair, '__len__') else 'non-list'} elements"
                    )

            # Convert computed values to expected units for comparison
            median_computed = result["median_obs"].to(self.calibration_target_estimates.units)

            # Get reported values (now lists)
            median_reported = self.calibration_target_estimates.median
            ci95_reported = self.calibration_target_estimates.ci95

            # Use relative tolerance of 1% for comparison
            rel_tol = 0.01

            # Handle array magnitudes
            median_mag = np.atleast_1d(median_computed.magnitude)

            # Element-wise comparison for median
            for i in range(len(median_reported)):
                if abs(median_mag[i] - median_reported[i]) > rel_tol * abs(median_reported[i]):
                    raise ComputedValueMismatchError(
                        f"Computed median[{i}] ({median_mag[i]:.4g}) does not match "
                        f"reported median[{i}] ({median_reported[i]:.4g}) within 1% tolerance"
                    )

            # Element-wise comparison for CI95
            for i in range(len(ci95_reported)):
                ci_computed = ci95_result[i]
                ci_rep = ci95_reported[i]

                # Get magnitudes (handle Pint quantities)
                lo_computed = (
                    ci_computed[0].magnitude
                    if hasattr(ci_computed[0], "magnitude")
                    else ci_computed[0]
                )
                hi_computed = (
                    ci_computed[1].magnitude
                    if hasattr(ci_computed[1], "magnitude")
                    else ci_computed[1]
                )

                if abs(lo_computed - ci_rep[0]) > rel_tol * abs(ci_rep[0]):
                    raise ComputedValueMismatchError(
                        f"Computed CI95[{i}] lower ({lo_computed:.4g}) does not match "
                        f"reported CI95[{i}] lower ({ci_rep[0]:.4g}) within 1% tolerance"
                    )

                if abs(hi_computed - ci_rep[1]) > rel_tol * abs(ci_rep[1]):
                    raise ComputedValueMismatchError(
                        f"Computed CI95[{i}] upper ({hi_computed:.4g}) does not match "
                        f"reported CI95[{i}] upper ({ci_rep[1]:.4g}) within 1% tolerance"
                    )

        except CalibrationTargetValidationError:
            # Re-raise all our custom validation errors
            raise
        except Exception as e:
            # Other execution errors - wrap in generic validation error
            raise CalibrationTargetValidationError(f"Error executing derivation_code: {e}") from e

        return self

