class CalibrationTargetEstimates(BaseModel):
    """
    Calibration target estimates with structured inputs and derivation.

    Each target is a SCALAR measurement: ``median`` is a length-1 list
    (``[42.0]``) and ``ci95`` a single ``[[lower, upper]]`` pair. The reduction
    from the observable time-series to that scalar is declared on the
    ``Observable`` (``readout_time`` or ``reduce_observable``) — there is no
    index dimension here. Multi-point data (time-course, dose-response) is
    authored as separate scalar targets, one per readout / arm.
    """

    model_config = ConfigDict(extra="forbid")

    median: List[float] = Field(
        description=(
            "COMPUTED median value from distribution_code, as a length-1 list " "(e.g. ``[42.0]``)."
        )
    )
    ci95: List[List[float]] = Field(
        description=(
            "COMPUTED 95% confidence interval from distribution_code, as a "
            "single ``[[lower, upper]]`` pair."
        )
    )
    units: str = Field(description="Units of the observable (Pint-parseable)")

    # Sample size metadata
    sample_size: int = Field(
        description=(
            "Sample size (n) for the measurement. CRITICAL for uncertainty quantification "
            "and pooling across studies.\n\n"
            "WHERE TO LOOK:\n"
            "- 'n = X' or 'N = X' in methods/results sections\n"
            "- Sample sizes in figure legends (e.g., 'n=5 per group')\n"
            "- Patient/subject counts in study design\n"
            "- Number of replicates in in vitro experiments\n"
            "- Number of animals per group in preclinical studies\n\n"
            "IF NOT REPORTED:\n"
            "- Check figure error bars - if SEM is reported, can back-calculate n from SD/SEM\n"
            "- Use conservative estimate based on study type\n"
            "- Document uncertainty in sample_size_rationale"
        )
    )
    sample_size_rationale: str = Field(
        description=(
            "Explanation of how sample_size was determined.\n\n"
            "Examples:\n"
            "- 'n=5 per group stated in Methods section 2.3'\n"
            "- 'n inferred from figure error bars using SEM formula: n = (SD/SEM)²'\n"
            "- 'n estimated as typical for this study type; not explicitly reported'\n"
            "- 'n=3 replicates per condition, standard for in vitro T cell assays'"
        )
    )

    inputs: List[EstimateInput] = Field(
        description=(
            "List of literature-extracted inputs used in distribution_code derivation. "
            "These are the VALUES REPORTED IN THE PAPER (mean, SD, median, IQR, trajectories, etc.) "
            "that are used to DERIVE the distribution via Monte Carlo.\n\n"
            "NOTE: These are values used by distribution_code, not by observable.code. "
            "observable.code uses observable.constants for grounded reference values."
        )
    )
    assumptions: List[ModelingAssumption] = Field(
        default_factory=list,
        description=(
            "List of modeling assumptions used in derivation. "
            "These are values NOT from the paper but needed for computation (e.g., n_mc_samples, "
            "assumed_cv when not reported). Each must have a rationale field explaining the choice."
        ),
    )
    distribution_code: str = Field(
        description=(
            "Python code defining a derive_distribution(inputs, ureg) function.\n"
            "inputs is a dict mapping input names to Pint Quantities (scalar or array).\n"
            "Must return dict with Pint Quantities:\n"
            "  - median_obs: median value as Pint Quantity\n"
            "  - ci95_lower: lower CI bound as Pint Quantity\n"
            "  - ci95_upper: upper CI bound as Pint Quantity\n\n"
            "Return scalar Quantities (each target is a scalar measurement).\n\n"
            "GOLDEN RULE: Reattach units immediately after sampling, then they propagate.\n"
            "np.median, np.percentile, np.mean, np.std all preserve Pint units.\n\n"
            "Example (scalar - single measurement):\n"
            "def derive_distribution(inputs, ureg):\n"
            "    import numpy as np\n"
            "    rng = np.random.default_rng(42)\n"
            "    mean = inputs['cd8_density_mean']\n"
            "    sd = inputs['cd8_density_sd']\n"
            "    # Reattach units immediately after sampling\n"
            "    samples = rng.normal(mean.magnitude, sd.magnitude, 10000) * mean.units\n"
            "    # np.median and np.percentile preserve units\n"
            "    return {\n"
            "        'median_obs': np.median(samples),\n"
            "        'ci95_lower': np.percentile(samples, 2.5),\n"
            "        'ci95_upper': np.percentile(samples, 97.5),\n"
            "    }\n\n"
            "Example (vector - time-course data):\n"
            "def derive_distribution(inputs, ureg):\n"
            "    import numpy as np\n"
            "    rng = np.random.default_rng(42)\n"
            "    means = inputs['cd8_density_mean']  # Quantity array\n"
            "    sds = inputs['cd8_density_sd']  # Quantity array\n"
            "    n_points = len(means.magnitude)\n"
            "    medians, lowers, uppers = [], [], []\n"
            "    for i in range(n_points):\n"
            "        # Reattach units immediately\n"
            "        samples = rng.normal(means.magnitude[i], sds.magnitude[i], 10000) * means.units\n"
            "        medians.append(np.median(samples).magnitude)\n"
            "        lowers.append(np.percentile(samples, 2.5).magnitude)\n"
            "        uppers.append(np.percentile(samples, 97.5).magnitude)\n"
            "    return {\n"
            "        'median_obs': np.array(medians) * means.units,\n"
            "        'ci95_lower': np.array(lowers) * means.units,\n"
            "        'ci95_upper': np.array(uppers) * means.units,\n"
            "    }\n\n"
            "POPULATION SAMPLE (for hierarchical inference), gated by population_spread:\n"
            "If population_spread='across_patient', you MUST ALSO return a 'samples' key\n"
            "holding the across-patient population draw (a Pint Quantity array, one value per\n"
            "patient-equivalent; 1-D for a scalar observable, or 2-D (n_patients, k) for a\n"
            "joint/compositional/trajectory observable). Its empirical spread (IQR/SD) is the\n"
            "population-variability (omega) signal, read directly with no parametric refit.\n"
            "It MUST be the POPULATION sample (dispersion = genuine patient-to-patient\n"
            "variability), not the sampling distribution of an estimator (e.g. a pooled-mean /\n"
            "SEM draw). median_obs/ci95 are UNAFFECTED — they remain the center + measurement\n"
            "uncertainty that non-hierarchical inference reads (and may legitimately differ\n"
            "from the sample spread, e.g. a meta-analytic anchor CI vs a wider population).\n"
            "If population_spread='center_only' (the default), do NOT return 'samples'."
        )
    )
    population_spread: Literal["across_patient", "center_only"] = Field(
        default="center_only",
        description=(
            "What this target's reported width MEANS, and whether it feeds the population-"
            "variability (omega) signal in hierarchical inference. This is an OPT-IN "
            "contract: a target contributes to omega only if it explicitly declares "
            "'across_patient' AND returns a population `samples` array.\n\n"
            "  - 'center_only' (default): the width is NOT genuine across-patient spread — "
            "e.g. a pooled-mean / SEM confidence interval (shrinks with n), a transcript-vs-"
            "protein method bound, or an assumed CV with no dispersion data. The target "
            "still constrains the population CENTER via `median`, but is EXCLUDED from omega "
            "conditioning. distribution_code must NOT return a `samples` key (that would "
            "contradict the declaration).\n"
            "  - 'across_patient': the width is real patient-to-patient variability, usable "
            "as the omega signal. distribution_code MUST return a `samples` key holding the "
            "across-patient population draw; the validator rejects the target otherwise. "
            "The conservative default keeps a fabricated or sample-size-shrinking width from "
            "silently polluting inferred diversity — you have to assert the spread is real."
        ),
    )
    observed_distribution: Optional[ObservedDistribution] = Field(
        default=None,
        description=(
            "Optional quantile-anchor representation of the reported distribution "
            "(median plus whatever scale anchors the source gives: IQR edges, quartiles, "
            "deciles, or a dense empirical quantile function). This is the general, "
            "shape-preserving data layer shared with SubmodelTarget; the framework can "
            "derive median/IQR/scale from it on demand. ADDITIVE and OPTIONAL — when "
            "omitted, the target behaves exactly as before (median/ci95 from "
            "distribution_code, omega gated by population_spread). Its `spread_source` "
            "carries finer provenance than the two-valued population_spread; when both "
            "are present they must agree on whether the width is genuine population spread "
            "(see resolved_spread_source)."
        ),
    )

    @property
    def resolved_spread_source(self) -> SpreadSource:
        """Unified spread provenance for downstream inference.

        Prefers the richer ``observed_distribution.spread_source`` when present;
        otherwise maps the legacy two-valued ``population_spread`` literal onto the
        shared enum. Lets consumers read one field regardless of which layer the
        target was authored against.
        """
        if self.observed_distribution is not None:
            return self.observed_distribution.spread_source
        return (
            SpreadSource.ACROSS_PATIENT
            if self.population_spread == "across_patient"
            else SpreadSource.CENTER_ONLY
        )

    @model_validator(mode="after")
    def validate_observed_distribution_consistency(self) -> "CalibrationTargetEstimates":
        """When both spread declarations are present, they must not contradict.

        ``observed_distribution`` is additive, so the legacy ``population_spread``
        contract still governs the ``samples`` gate; this only forbids the two from
        disagreeing about whether the reported width is genuine population spread.
        """
        od = self.observed_distribution
        if od is None:
            return self
        feeds = od.feeds_population_spread
        declared = self.population_spread == "across_patient"
        if feeds != declared:
            raise ValueError(
                f"observed_distribution.spread_source='{od.spread_source.value}' "
                f"({'feeds' if feeds else 'does not feed'} population spread) contradicts "
                f"population_spread='{self.population_spread}'. Align them: a population "
                f"spread source (across_patient / biological_experimental) requires "
                f"population_spread='across_patient'; a center/technical source requires "
                f"'center_only'."
            )
        return self

    @model_validator(mode="after")
    def validate_bounded_observable_uses_logit_normal(self) -> "CalibrationTargetEstimates":
        """A bounded observable's population spread (``moments`` form) must use
        ``shape: logit_normal``, not normal/lognormal.

        Parity with the SubmodelTarget validator of the same name: for a
        fraction / proportion / probability / percent observable, ``normal`` puts
        mass outside the bound and ``lognormal`` is unbounded above; ``logit_normal``
        keeps expanded quartiles in (0, 1). Only applies to the ``moments`` form —
        the ``quantiles`` form carries the empirical shape directly and is exempt.
        """
        od = self.observed_distribution
        if od is None or od.moments is None:
            return self
        if od.moments.shape == DistributionShape.LOGIT_NORMAL:
            return self
        BOUNDED_UNITS = {"percent", "%", "fraction", "proportion", "probability"}
        if (self.units or "").strip().lower() not in BOUNDED_UNITS:
            return self
        raise ValueError(
            f"Observable units='{self.units}' are a bounded fraction/percentage, but "
            f"observed_distribution.moments uses shape='{od.moments.shape.value}'. "
            "Bounded observables must use shape='logit_normal', which expands quartiles "
            "in logit space so they never escape (0, 1); normal puts mass outside the "
            "bound and lognormal is unbounded above. logit_normal requires center in "
            "(0, 1) with center_type='median' — express a percent as a fraction (12% -> 0.12)."
        )

    @field_validator("sample_size")
    @classmethod
    def validate_sample_size_positive(cls, v: int) -> int:
        """Validate sample size is at least 1."""
        if v < 1:
            raise ValueError(f"sample_size must be at least 1, got {v}")
        return v

    @model_validator(mode="after")
    def validate_scalar_output(self) -> "CalibrationTargetEstimates":
        """Validator: each target is a scalar — ``median`` is length-1 and
        ``ci95`` is a single ``[lower, upper]`` pair."""
        if len(self.median) != 1:
            raise ValueError(
                f"median must be a length-1 list (a scalar target), got length {len(self.median)}. "
                f"Multi-point data is authored as separate scalar targets, one per readout / arm."
            )
        if len(self.ci95) != 1:
            raise ValueError(
                f"ci95 must be a single [lower, upper] pair, got {len(self.ci95)} entries."
            )
        if len(self.ci95[0]) != 2:
            raise ValueError(
                f"ci95 must be [[lower, upper]], got {len(self.ci95[0])} elements in the pair."
            )

        return self

    @model_validator(mode="after")
    def validate_derived_arithmetic_inputs(self) -> "CalibrationTargetEstimates":
        """
        Validate derived_arithmetic inputs: require formula + source_inputs,
        check that source_inputs reference existing inputs, and verify that
        evaluating the formula produces the declared value.

        Also rejects formula/source_inputs on non-derived_arithmetic inputs.

        Only scalar inputs are supported for derived_arithmetic (vector-valued
        formulas would need element-wise evaluation, not currently implemented).
        """
        import math
        import re

        input_map = {inp.name: inp.value for inp in self.inputs}
        errors = []

        for inp in self.inputs:
            if inp.input_type == InputType.DERIVED_ARITHMETIC:
                if not inp.formula:
                    errors.append(
                        f"Input '{inp.name}' has input_type='derived_arithmetic' "
                        f"but no formula provided."
                    )
                if not inp.source_inputs:
                    errors.append(
                        f"Input '{inp.name}' has input_type='derived_arithmetic' "
                        f"but no source_inputs provided."
                    )
                if not inp.formula or not inp.source_inputs:
                    continue

                # Check all source_inputs exist
                missing = [s for s in inp.source_inputs if s not in input_map]
                if missing:
                    errors.append(
                        f"Input '{inp.name}': source_inputs {missing} not found in inputs."
                    )
                    continue

                # Only scalar derivations supported
                if isinstance(inp.value, list):
                    errors.append(
                        f"Input '{inp.name}': derived_arithmetic inputs must be scalar "
                        f"(vector-valued formulas not supported). Declare one derived input "
                        f"per index point instead."
                    )
                    continue
                for s in inp.source_inputs:
                    if isinstance(input_map[s], list):
                        errors.append(
                            f"Input '{inp.name}': source_input '{s}' is vector-valued; "
                            f"derived_arithmetic formulas only support scalar sources."
                        )
                if any(isinstance(input_map[s], list) for s in inp.source_inputs):
                    continue

                # Evaluate formula in a safe namespace
                namespace = {s: input_map[s] for s in inp.source_inputs}
                safe_builtins = {
                    "abs": abs,
                    "min": min,
                    "max": max,
                    "log": math.log,
                    "log2": math.log2,
                    "log10": math.log10,
                    "sqrt": math.sqrt,
                    "exp": math.exp,
                    "pi": math.pi,
                }
                namespace.update(safe_builtins)

                try:
                    formula_names = set(re.findall(r"\b([a-zA-Z_]\w*)\b", inp.formula))
                    allowed_names = set(inp.source_inputs) | set(safe_builtins.keys())
                    unknown = formula_names - allowed_names
                    if unknown:
                        errors.append(
                            f"Input '{inp.name}': formula references unknown names "
                            f"{unknown}. Only source_inputs and math functions "
                            f"(abs, min, max, log, sqrt, exp) are allowed."
                        )
                        continue

                    computed = eval(inp.formula, {"__builtins__": {}}, namespace)  # noqa: S307
                    if inp.value == 0:
                        if abs(computed) > 1e-10:
                            errors.append(
                                f"Input '{inp.name}': formula '{inp.formula}' "
                                f"evaluates to {computed}, but declared value is 0."
                            )
                    elif abs(computed - inp.value) / abs(inp.value) > 0.01:
                        errors.append(
                            f"Input '{inp.name}': formula '{inp.formula}' evaluates to "
                            f"{computed}, but declared value is {inp.value} (>1% mismatch)."
                        )
                except Exception as e:
                    errors.append(
                        f"Input '{inp.name}': formula '{inp.formula}' failed to evaluate: {e}"
                    )
            else:
                # Non-derived_arithmetic inputs must not have formula/source_inputs
                if inp.formula is not None:
                    errors.append(
                        f"Input '{inp.name}' has input_type='{inp.input_type.value}' but "
                        f"a formula field. formula is only valid for derived_arithmetic inputs."
                    )
                if inp.source_inputs is not None:
                    errors.append(
                        f"Input '{inp.name}' has input_type='{inp.input_type.value}' but "
                        f"a source_inputs field. source_inputs is only valid for "
                        f"derived_arithmetic inputs."
                    )

        if errors:
            raise ValueError("\n".join(errors))

        return self


class CalibrationTarget(BaseModel):
    """
    A calibration target: a raw observable extracted from literature.

    Used to calibrate QSP model parameters via Bayesian inference.

    Excludes header fields that are added during unpacking:
    - calibration_target_id, cancer_type
    - tags, derivation_id, derivation_timestamp
    """

    model_config = ConfigDict(extra="forbid")

    # --- Observable fields (LLM-generated) ---
    empirical_data: CalibrationTargetEstimates = Field(
        description="Observable estimates with inputs and derivation"
    )

    # ========================================================================
    # Field definitions
    # ========================================================================

    # --- Observable specification (LLM-generated) ---
    observable: Observable = Field(
        description=(
            "How to compute the experimental observable from full model species. "
            "Defines the code that transforms model output to match literature measurements."
        )
    )

    # --- Experimental context (LLM-generated, grouped for distance calculations) ---
    experimental_context: ExperimentalContext = Field(
        description="Experimental context of the observable"
    )

    # --- Scenario (optional, for intervention tracking) ---
    scenario: Optional[Scenario] = Field(
        default=None,
        description=(
            "Experimental scenario with interventions. Optional - can be omitted if "
            "no interventions or if intervention details are captured in study_interpretation."
        ),
    )

    # --- Narrative and assumptions (LLM-generated) ---
    study_interpretation: str = Field(
        description=(
            "Overall interpretation of the study and how it relates to the model.\n"
            "Explain what observable is being measured, how the experimental context maps "
            "to the model, and any key methodological considerations.\n"
            "Keep focused on the scientific interpretation, not assumptions or limitations."
        )
    )

    key_assumptions: List[str] = Field(
        min_length=1,
        description=(
            "List of key assumptions made in extracting or interpreting this calibration target.\n"
            "Include both biological assumptions (e.g., cell type equivalence) and "
            "statistical/methodological assumptions (e.g., normal distribution assumed).\n"
            "Examples:\n"
            "- 'CD8+ T cells measured include both effector and exhausted phenotypes'\n"
            "- 'Normal distribution assumed for positive-only data (may introduce bias)'\n"
            "- 'Mouse data assumed transferable to human with 5-10x rate reduction'"
        ),
    )

    key_study_limitations: List[str] = Field(
        default_factory=list,
        description=(
            "List of study limitations that affect the calibration target validity.\n"
            "Focus on issues that could bias the estimates or limit generalizability.\n"
            "Examples:\n"
            "- 'Single-center cohort (n~15), limited generalizability'\n"
            "- 'Values estimated from figures, not tabulated data'\n"
            "- 'Bulk CD3+ assay, not CD8-specific'"
        ),
    )

    # --- Epistemic basis ---
    epistemic_basis: Literal["literature", "mechanistic"] = Field(
        default="literature",
        description=(
            "How this target's empirical_data is grounded.\n\n"
            "- 'literature' (default): a primary publication directly measures the\n"
            "  observable's distribution. Requires primary_data_source with a DOI;\n"
            "  empirical_data.inputs values must appear in their value_snippets.\n"
            "- 'mechanistic': encodes a soft prior from biological reasoning rather\n"
            "  than a published measurement (e.g., 'untreated tumors do not\n"
            "  spontaneously regress', 'PD-1 blockade increases T-cell killing\n"
            "  capacity'). primary_data_source may be null. value_snippet checks are\n"
            "  skipped. key_assumptions MUST contain the mechanistic rationale and\n"
            "  describe the chosen distribution width. Distributions should be\n"
            "  deliberately wide so the target nudges rather than dominates the\n"
            "  likelihood. Live in calibration_targets/mechanistic/ to keep them\n"
            "  visibly distinct from literature targets.\n\n"
            "Note: 'mechanistic' is NOT a backdoor for unverified citations or\n"
            "fabricated CIs — if a paper exists, use 'literature'."
        ),
    )

    # --- Sources (LLM-generated, required for literature targets) ---
    primary_data_source: Optional[Source] = Field(
        default=None,
        description=(
            "Primary data source (the paper where this observable was measured). "
            "Required when epistemic_basis='literature'; may be null when "
            "epistemic_basis='mechanistic'."
        ),
    )
    secondary_data_sources: List[SecondarySource] = Field(
        default_factory=list, description="Secondary data sources (reference values, constants)"
    )

    # --- Footer fields (metadata about the file, not LLM-generated) ---
    calibration_target_id: Optional[str] = Field(
        default=None, description="Unique calibration target identifier"
    )
    cancer_type: Optional[str] = Field(default=None, description="Cancer type (e.g., 'PDAC')")
    tags: List[str] = Field(default_factory=list, description="Metadata tags")
    derivation_id: Optional[str] = Field(default=None, description="Unique derivation identifier")
    derivation_timestamp: Optional[str] = Field(
        default=None, description="ISO timestamp of derivation"
    )

    # --- Extraction metadata (optional, recorded by extraction pipeline) ---
    logfire_trace_id: Optional[str] = Field(
        default=None, description="Logfire trace ID for extraction provenance"
    )
    extraction_model: Optional[str] = Field(
        default=None, description="LLM model used for extraction"
    )
    extraction_reasoning_effort: Optional[str] = Field(
        default=None, description="Reasoning effort level used during extraction"
    )
    exclusion_reason: Optional[str] = Field(
        default=None, description="Reason this target was excluded from active use"
    )

    @classmethod
    def get_header_fields(cls) -> set[str]:
        """Get set of field names that are headers (from CalibrationTargetFooters)."""
        return set(CalibrationTargetFooters.model_fields.keys())

    def split(self) -> tuple[CalibrationTargetFooters, dict]:
        """
        Split into headers and content.

        Returns:
            Tuple of (headers_model, content_dict)
        """
        header_fields = self.get_header_fields()
        all_data = self.model_dump()

        # Extract headers
        header_data = {k: v for k, v in all_data.items() if k in header_fields}
        headers = CalibrationTargetFooters(**header_data)

        # Extract content
        content = {k: v for k, v in all_data.items() if k not in header_fields}

        return headers, content

    @classmethod
    def from_split(cls, headers: CalibrationTargetFooters, content: dict) -> "CalibrationTarget":
        """
        Create CalibrationTarget from headers and content.

        Args:
            headers: CalibrationTargetFooters instance
            content: Content dictionary (LLM-generated fields)

        Returns:
            Complete CalibrationTarget instance
        """
        return cls(**{**headers.model_dump(), **content})

