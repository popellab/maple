    @model_validator(mode="after")
    def warn_observation_sd_unreasonable(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Flag unreasonable implied CV from CI95 width.

        Computes implied coefficient of variation from the CI95 width relative
        to the median. Warns if CV > 200% (suspiciously wide) or CV < 1%
        (suspiciously narrow, possibly a copy-paste error).
        """
        import warnings

        for i, (med, ci) in enumerate(zip(self.empirical_data.median, self.empirical_data.ci95)):
            if med == 0:
                continue
            ci_width = ci[1] - ci[0]
            # Approximate SD from CI95 width: SD ≈ CI_width / (2 * 1.96)
            implied_sd = ci_width / 3.92
            implied_cv = abs(implied_sd / med)

            idx_label = f"[{i}]" if len(self.empirical_data.median) > 1 else ""

            if implied_cv > 2.0:
                warnings.warn(
                    f"Implied CV{idx_label} = {implied_cv:.0%} (from CI95 width / median). "
                    f"CV > 200% is unusually large — verify that ci95 and median are in the same units "
                    f"and that the uncertainty hasn't been inflated.",
                    UserWarning,
                )
            elif implied_cv < 0.01:
                warnings.warn(
                    f"Implied CV{idx_label} = {implied_cv:.2%} (from CI95 width / median). "
                    f"CV < 1% is unusually small — verify that ci95 bounds differ from the median "
                    f"and aren't copy-paste duplicates.",
                    UserWarning,
                )

        return self

    @model_validator(mode="after")
    def validate_source_relevance_warnings(self) -> "CalibrationTarget":
        """Emit warnings for source-relevance issues (non-peer-reviewed, cross-indication,
        cross-species, low TME compatibility, perturbation without justification)."""
        if self.primary_data_source is None or self.primary_data_source.source_relevance is None:
            return self

        import warnings

        sr = self.primary_data_source.source_relevance

        if sr.source_quality == SourceQuality.NON_PEER_REVIEWED:
            warnings.warn(
                "source_relevance.source_quality is 'non_peer_reviewed'. "
                "Prefer peer-reviewed primary literature for calibration targets. "
                "If this source must be used, document the rationale in key_assumptions.",
                UserWarning,
            )

        if sr.indication_match in (IndicationMatch.PROXY, IndicationMatch.UNRELATED):
            warnings.warn(
                f"Cross-indication extrapolation: indication_match='{sr.indication_match.value}'. "
                "Translation sigma inflation will be applied automatically during prior construction.",
                UserWarning,
            )

        if sr.species_source != sr.species_target:
            warnings.warn(
                f"Cross-species extrapolation: {sr.species_source} → {sr.species_target}. "
                "Translation sigma inflation will be applied automatically during prior construction.",
                UserWarning,
            )

        if (
            sr.perturbation_type in (PerturbationType.PHARMACOLOGICAL, PerturbationType.GENETIC)
            and not sr.perturbation_relevance
        ):
            warnings.warn(
                f"source_relevance.perturbation_type is '{sr.perturbation_type.value}' but "
                "perturbation_relevance is not provided. Document how the perturbed "
                "measurement relates to the physiological parameter being estimated.",
                UserWarning,
            )

        if sr.tme_compatibility == TMECompatibility.LOW and not sr.tme_compatibility_notes:
            warnings.warn(
                "source_relevance.tme_compatibility is 'low' but tme_compatibility_notes "
                "is not provided. Document the TME differences and their expected impact.",
                UserWarning,
            )

        return self

    @model_validator(mode="after")
    def validate_input_values_in_snippets(self) -> "CalibrationTarget":
        """Validator: Check that input values appear in their value_snippet.

        Ensures data integrity by verifying that extracted values can be found
        in the source text. Handles multiple numeric formats (decimal, scientific,
        percentage, rounded variations).

        Checks inputs in empirical_data.inputs (EstimateInput).

        Raises SnippetValueMismatchError if a value is not found in its snippet.

        Skipped entirely for mechanistic targets — their inputs (when present)
        encode mechanistic reasoning rather than literal paper-extracted values.
        """
        if self.epistemic_basis == "mechanistic":
            return self

        def check_inputs(inputs, location_prefix: str):
            """Helper to check value_snippet for a list of inputs."""
            from maple.core.calibration.shared_models import InputType

            for inp in inputs:
                value = inp.value
                snippet = inp.value_snippet

                if not snippet:
                    continue

                # Skip validation for inferred estimates (values interpreted from qualitative text)
                if hasattr(inp, "input_type") and inp.input_type == InputType.INFERRED_ESTIMATE:
                    continue

                # Skip validation for figure-sourced values (not expected in text snippets)
                if hasattr(inp, "source_type") and inp.source_type == SourceType.FIGURE:
                    continue

                # Handle both scalar and vector-valued inputs
                values_to_check = [value] if not isinstance(value, list) else value

                missing_values = []
                for val in values_to_check:
                    if not check_value_in_text(snippet, val):
                        missing_values.append(val)

                if missing_values:
                    raise SnippetValueMismatchError(
                        f"{location_prefix} input '{inp.name}' has value(s) not found in value_snippet.\n"
                        f"  Missing values: {missing_values}\n"
                        f"  Snippet: '{snippet[:200]}{'...' if len(snippet) > 200 else ''}'\n"
                        f"The value_snippet must contain the exact values extracted from the paper.\n"
                        f"Check for:\n"
                        f"  - Typos in the extracted value\n"
                        f"  - Different numeric format in the paper (e.g., '2.5×10⁻³' vs '0.0025')\n"
                        f"  - Value reported in different units that need conversion\n"
                        f"  - Snippet truncated before the value appears\n"
                        f"  - If value is INTERPRETED from qualitative text, use input_type='inferred_estimate'\n"
                        f"  - If value is READ FROM A FIGURE (plot/chart), use source_type='figure' with figure_id and extraction_method"
                    )

        # Check empirical_data.inputs
        check_inputs(self.empirical_data.inputs, "empirical_data")

        return self

    @model_validator(mode="after")
    def validate_derivation_code(self) -> "CalibrationTarget":
        """
        Validator: Check that derivation_code executes, returns correct structure and units,
        and computed values match reported values (element-wise for vector data).
        """
        from maple.core.unit_registry import make_quantity, ureg
        from maple.core.calibration.code_validator import (
            CodeType,
            validate_code_block,
        )

        # Use CodeValidator for syntax and signature checks
        result = validate_code_block(
            self.empirical_data.distribution_code,
            CodeType.DISTRIBUTION,
            check_hardcoded=True,  # Check for hardcoded constants with units
            check_execution=False,  # We do custom execution below
        )

        if not result.passed:
            errors = result.get_errors()
            if errors:
                first_error = errors[0]
                if first_error.category == "syntax":
                    raise CodeSyntaxError(
                        f"distribution_code has syntax error: {first_error.message}"
                    )
                elif first_error.category == "signature":
                    raise CodeStructureError(first_error.message)
                elif first_error.category == "hardcoded":
                    # Provide specific guidance for distribution_code
                    raise CodeStructureError(
                        f"{first_error.message}\n\n"
                        "IMPORTANT: distribution_code is for STATISTICAL derivation only.\n"
                        "It converts literature-reported values (mean ± SD) into uncertainty distributions.\n"
                        "It should NOT need model compartment volumes (V_T, V_C, V_P) or physical constants.\n\n"
                        "If you're seeing this error with compartment volumes like 'V_T = 1.0 * ureg.milliliter',\n"
                        "you may be confusing distribution_code with observable.code or submodel.code:\n"
                        "  • distribution_code: Statistical only (mean → samples → median/CI)\n"
                        "  • observable.code: Model simulation (uses compartment volumes, species)\n"
                        "  • submodel.code: ODE dynamics (uses model parameters)\n\n"
                        "If this constant IS needed for statistical derivation, declare it in:\n"
                        "  • empirical_data.inputs (if from literature)\n"
                        "  • empirical_data.assumptions (if a modeling choice)"
                    )
                else:
                    raise CodeStructureError(first_error.message)

        # Execute with mock inputs to test structure and units
        try:
            # Create mock inputs dict with Pint quantities
            # Merge both literature inputs and modeling assumptions into a single dict
            mock_inputs = {}
            # Add literature inputs
            for inp in self.empirical_data.inputs:
                if isinstance(inp.value, list):
                    mock_inputs[inp.name] = np.array(inp.value) * ureg(inp.units)
                else:
                    mock_inputs[inp.name] = make_quantity(inp.value, inp.units)
            # Add modeling assumptions
            for assumption in self.empirical_data.assumptions:
                if isinstance(assumption.value, list):
                    mock_inputs[assumption.name] = np.array(assumption.value) * ureg(
                        assumption.units
                    )
                else:
                    mock_inputs[assumption.name] = assumption.value * ureg(assumption.units)

            # Execute function
            local_scope = {"ureg": ureg, "np": np}
            exec(self.empirical_data.distribution_code, local_scope)
            derive_fn = local_scope["derive_distribution"]

            result = derive_fn(mock_inputs, ureg)

            # Check result is dict with required keys
            if not isinstance(result, dict):
                raise ReturnStructureError("Function must return a dict")

            required_keys = {"median_obs", "ci95_lower", "ci95_upper"}
            if not required_keys.issubset(result.keys()):
                raise ReturnStructureError(
                    f"Function must return dict with keys: {required_keys}. Got: {result.keys()}"
                )

            # Check all returned values have units
            for key in required_keys:
                if not hasattr(result[key], "units"):
                    raise MissingUnitsError(
                        f"Result['{key}'] must be a Pint Quantity with units.\n\n"
                        "The distribution_code returned a plain number without units.\n"
                        "Common cause: Forgot to reattach units after sampling.\n\n"
                        "Fix: Reattach units immediately after sampling.\n\n"
                        "Example:\n"
                        "  samples = rng.normal(mean.magnitude, sd.magnitude, n) * mean.units\n"
                        "  return {\n"
                        "      'median_obs': np.median(samples),\n"
                        "      'ci95_lower': np.percentile(samples, 2.5),\n"
                        "      'ci95_upper': np.percentile(samples, 97.5),\n"
                        "  }"
                    )

            # Check dimensionality matches expected units
            expected_quantity = 1.0 * ureg(self.empirical_data.units)

            for key in required_keys:
                if result[key].dimensionality != expected_quantity.dimensionality:
                    raise DimensionalityMismatchError(
                        f"{key} unit dimensionality mismatch:\n"
                        f"  Expected: {self.empirical_data.units} ({expected_quantity.dimensionality})\n"
                        f"  Got: {result[key].units} ({result[key].dimensionality})"
                    )

            # Convert computed values to expected units for comparison
            median_computed = result["median_obs"].to(self.empirical_data.units)
            ci95_lower_computed = result["ci95_lower"].to(self.empirical_data.units)
            ci95_upper_computed = result["ci95_upper"].to(self.empirical_data.units)

            # Get reported values
            median_reported = self.empirical_data.median
            ci95_reported = self.empirical_data.ci95

            # Tolerances: 1% for median (deterministic), 10% for CI bounds (Monte Carlo variance)
            median_rel_tol = 0.01
            ci95_rel_tol = 0.10

            # Handle array magnitudes (works for both scalar and vector)
            median_mag = np.atleast_1d(median_computed.magnitude)
            ci95_lower_mag = np.atleast_1d(ci95_lower_computed.magnitude)
            ci95_upper_mag = np.atleast_1d(ci95_upper_computed.magnitude)

            # Check lengths match
            n_expected = len(median_reported)
            if len(median_mag) != n_expected:
                raise ReturnStructureError(
                    f"median_obs length mismatch: expected {n_expected}, got {len(median_mag)}"
                )
            if len(ci95_lower_mag) != n_expected:
                raise ReturnStructureError(
                    f"ci95_lower length mismatch: expected {n_expected}, got {len(ci95_lower_mag)}"
                )
            if len(ci95_upper_mag) != n_expected:
                raise ReturnStructureError(
                    f"ci95_upper length mismatch: expected {n_expected}, got {len(ci95_upper_mag)}"
                )

            # Element-wise comparison for median (1% tolerance)
            for i in range(n_expected):
                if abs(median_mag[i] - median_reported[i]) > median_rel_tol * abs(
                    median_reported[i]
                ):
                    raise ComputedValueMismatchError(
                        f"Computed median[{i}] ({median_mag[i]:.4g}) does not match "
                        f"reported median[{i}] ({median_reported[i]:.4g}) within 1% tolerance"
                    )

            # Element-wise comparison for CI95 (10% tolerance for Monte Carlo variance)
            for i in range(n_expected):
                ci_rep = ci95_reported[i]

                if abs(ci95_lower_mag[i] - ci_rep[0]) > ci95_rel_tol * abs(ci_rep[0]):
                    raise ComputedValueMismatchError(
                        f"Computed ci95_lower[{i}] ({ci95_lower_mag[i]:.4g}) does not match "
                        f"reported CI95[{i}] lower ({ci_rep[0]:.4g}) within 10% tolerance"
                    )

                if abs(ci95_upper_mag[i] - ci_rep[1]) > ci95_rel_tol * abs(ci_rep[1]):
                    raise ComputedValueMismatchError(
                        f"Computed ci95_upper[{i}] ({ci95_upper_mag[i]:.4g}) does not match "
                        f"reported CI95[{i}] upper ({ci_rep[1]:.4g}) within 10% tolerance"
                    )

            # --- Population sample, gated by population_spread (hierarchical omega) ---
            # 'across_patient' MUST return a 'samples' key (the population draw feeding
            # omega); 'center_only' MUST NOT (a population sample would contradict the
            # declaration that the width is center / measurement uncertainty).
            population_spread = self.empirical_data.population_spread
            has_samples = "samples" in result
            if population_spread == "across_patient" and not has_samples:
                raise ReturnStructureError(
                    "population_spread='across_patient' requires distribution_code to "
                    "return a 'samples' key — the across-patient population draw used as "
                    "the omega signal in hierarchical inference. Return one, or set "
                    "population_spread='center_only' if the width is not a real spread."
                )
            if population_spread == "center_only" and has_samples:
                raise ReturnStructureError(
                    "population_spread='center_only' must NOT return a 'samples' key: a "
                    "population sample contradicts the declaration that the width is "
                    "center / measurement uncertainty. Remove 'samples', or set "
                    "population_spread='across_patient' if the spread is genuine."
                )
            if has_samples:
                samples = result["samples"]
                if not hasattr(samples, "units"):
                    raise MissingUnitsError(
                        "Result['samples'] must be a Pint Quantity array with units "
                        "(the across-patient population draw, in the observable's units)."
                    )
                if samples.dimensionality != expected_quantity.dimensionality:
                    raise DimensionalityMismatchError(
                        f"samples unit dimensionality mismatch:\n"
                        f"  Expected: {self.empirical_data.units} ({expected_quantity.dimensionality})\n"
                        f"  Got: {samples.units} ({samples.dimensionality})"
                    )
                samp_mag = np.asarray(samples.to(self.empirical_data.units).magnitude, dtype=float)
                # 1-D for a scalar observable, or 2-D (n_patients, k) for a joint /
                # compositional / trajectory observable (margins consumed today; the
                # joint is available when the stage conditions on a covariance).
                if samp_mag.ndim not in (1, 2):
                    raise ReturnStructureError(
                        f"samples must be 1-D (scalar observable) or 2-D (n_patients, k); "
                        f"got {samp_mag.ndim}-D with shape {samp_mag.shape}"
                    )
                finite = samp_mag[np.isfinite(samp_mag)]
                if finite.size < 100:
                    raise ReturnStructureError(
                        f"samples has too few finite values ({finite.size}); need >=100 "
                        f"for a stable empirical population spread"
                    )
                if np.std(finite) == 0:
                    raise ReturnStructureError(
                        "samples has zero variance (all identical) — not a usable population "
                        "spread; set population_spread='center_only' if no real spread exists"
                    )
                # Tie the declared sample to the declared center: for a scalar (1-D)
                # target its median must match the reported median within MC tolerance.
                if samp_mag.ndim == 1 and n_expected == 1:
                    samp_median = float(np.median(finite))
                    if abs(samp_median - median_reported[0]) > ci95_rel_tol * abs(
                        median_reported[0]
                    ):
                        raise ComputedValueMismatchError(
                            f"median(samples) ({samp_median:.4g}) does not match reported "
                            f"median ({median_reported[0]:.4g}) within 10% — the declared "
                            f"'samples' array must be the population draw the median/CI summarize"
                        )

        except CalibrationTargetValidationError:
            # Re-raise all our custom validation errors
            raise
        except Exception as e:
            # Provide helpful guidance for common errors
            error_msg = str(e)
            guidance = ""

            if "setting an array element with a sequence" in error_msg:
                guidance = (
                    "\n\nThis error typically occurs when array structures are inconsistent.\n"
                    "Common cause: Mixing Pint Quantities and plain numbers.\n\n"
                    "Fix: Return flat Pint Quantities for each key.\n\n"
                    "Example:\n"
                    "  samples = rng.normal(mean.magnitude, sd.magnitude, n) * mean.units\n"
                    "  return {\n"
                    "      'median_obs': np.median(samples),\n"
                    "      'ci95_lower': np.percentile(samples, 2.5),\n"
                    "      'ci95_upper': np.percentile(samples, 97.5),\n"
                    "  }"
                )
            elif "unsupported operand type" in error_msg or "cannot convert" in error_msg.lower():
                guidance = (
                    "\n\nThis error often occurs when mixing Pint Quantities with plain numbers.\n"
                    "Ensure all values in calculations have consistent units.\n"
                    "Use inputs['name'].magnitude to extract plain numbers if needed."
                )
            elif "KeyError" in error_msg or "key" in error_msg.lower():
                guidance = (
                    "\n\nThis error suggests a missing input in the inputs dict.\n"
                    "Check that all variables used in distribution_code are defined in:\n"
                    "  - empirical_data.inputs (for literature values)\n"
                    "  - empirical_data.assumptions (for modeling assumptions like n_mc_samples)"
                )

            raise CalibrationTargetValidationError(
                f"Error executing distribution_code: {e}{guidance}"
            ) from e

        return self

    @model_validator(mode="after")
    def validate_source_refs(self) -> "CalibrationTarget":
        """Validator: Check all source_refs in empirical_data.inputs point to defined sources."""
        # Build set of valid source tags from literature sources
        valid_tags: set[str] = set()
        if self.primary_data_source is not None:
            valid_tags.add(self.primary_data_source.source_tag)
        valid_tags.update(s.source_tag for s in self.secondary_data_sources)

        def check_refs(inputs, location_prefix: str):
            """Helper to check source_refs for a list of inputs."""
            for inp in inputs:
                if inp.source_ref not in valid_tags:
                    raise SourceRefError(
                        f"{location_prefix} input '{inp.name}' has source_ref '{inp.source_ref}' which is not defined.\n"
                        f"Valid source tags: {sorted(valid_tags)}\n"
                        f"Add the source to primary_data_source or secondary_data_sources.\n"
                        f"For values not from literature, use the 'assumptions' field instead."
                    )

        # Check empirical_data.inputs
        check_refs(self.empirical_data.inputs, "empirical_data")

        return self

    @model_validator(mode="after")
    def validate_constant_source_refs(self, info: ValidationInfo) -> "CalibrationTarget":
        """
        Validator (ERROR): Verify observable constant sources are traceable.

        Every observable constant must have a verifiable source:
        - reference_db: reference_db_name must match an entry in reference_values.yaml
        - derived_from_reference_db: all reference_db_names must match entries
        - literature: source_tag must match a defined source in this target

        This prevents ungrounded "modeling assumptions" from entering constants.
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        if not self.observable.constants:
            return self

        # Build set of valid literature source tags
        valid_tags: set[str] = set()
        if self.primary_data_source is not None:
            valid_tags.add(self.primary_data_source.source_tag)
        valid_tags.update(s.source_tag for s in self.secondary_data_sources)

        # Get reference DB names from validation context (if available)
        # reference_db is a dict[str, float] mapping name → value
        reference_db_names = set()
        if info.context and "reference_db" in info.context:
            reference_db_names = set(info.context["reference_db"].keys())

        for const in self.observable.constants:
            if const.source_type == ConstantSourceType.LITERATURE:
                if const.source_tag not in valid_tags:
                    raise SourceRefError(
                        f"Observable constant '{const.name}' has source_tag '{const.source_tag}' "
                        f"which is not defined in this target's sources.\n"
                        f"Valid source tags: {sorted(valid_tags)}\n"
                        f"Add the source to secondary_data_sources, or use "
                        f"source_type='reference_db' with a reference_values.yaml entry."
                    )

            elif const.source_type == ConstantSourceType.REFERENCE_DB:
                if reference_db_names and const.reference_db_name not in reference_db_names:
                    raise SourceRefError(
                        f"Observable constant '{const.name}' references "
                        f"reference_db_name='{const.reference_db_name}' which was not found "
                        f"in reference_values.yaml.\n"
                        f"Available entries: {sorted(reference_db_names)}\n"
                        f"Add the entry to reference_values.yaml or use a different source."
                    )

            elif const.source_type == ConstantSourceType.DERIVED_FROM_REFERENCE_DB:
                if reference_db_names and const.reference_db_names:
                    missing = [
                        name for name in const.reference_db_names if name not in reference_db_names
                    ]
                    if missing:
                        raise SourceRefError(
                            f"Observable constant '{const.name}' derives from reference DB entries "
                            f"that were not found: {missing}\n"
                            f"Available entries: {sorted(reference_db_names)}\n"
                            f"Add the missing entries to reference_values.yaml."
                        )

        return self

