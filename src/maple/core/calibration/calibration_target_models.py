    @model_validator(mode="after")
    def validate_large_variance_documented(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Check if large variance is documented in limitations.

        CV > 50% suggests substantial variability that should be explained.
        Only applies to scalar inputs (vector inputs are checked per-element elsewhere).
        """
        import warnings

        # Find mean and SD/SE inputs (only scalar inputs)
        mean_input = None
        std_input = None

        for inp in self.empirical_data.inputs:
            # Skip vector-valued inputs for this check
            if isinstance(inp.value, list):
                continue

            name_lower = inp.name.lower()
            if "mean" in name_lower and mean_input is None:
                mean_input = inp
            if any(x in name_lower for x in ["sd", "std", "se", "stderr", "stdev"]):
                std_input = inp

        if mean_input and std_input:
            mean_val = mean_input.value
            std_val = std_input.value

            # Both must be scalar at this point (we skipped lists above)
            if not isinstance(mean_val, list) and not isinstance(std_val, list) and mean_val != 0:
                # Calculate CV (coefficient of variation)
                cv = abs(std_val / mean_val)

                if cv > 0.5:  # CV > 50%
                    # Check if key_study_limitations or study_interpretation mention variance/uncertainty
                    limitations_text = " ".join(self.key_study_limitations).lower()
                    interpretation_lower = self.study_interpretation.lower()
                    combined_text = limitations_text + " " + interpretation_lower
                    variance_keywords = [
                        "variance",
                        "variability",
                        "uncertain",
                        "cv",
                        "wide",
                        "heterogen",
                        "variation",
                        "spread",
                        "dispersion",
                    ]

                    if not any(keyword in combined_text for keyword in variance_keywords):
                        warnings.warn(
                            f"Large coefficient of variation (CV = {cv:.1%}) detected but not discussed in key_study_limitations. "
                            f"Consider adding a limitation explaining whether this reflects biological variability, "
                            f"measurement error, or heterogeneous population.",
                            UserWarning,
                        )

        return self

    @model_validator(mode="after")
    def validate_distribution_choice_for_size_data(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Check if normal distribution used for size data.

        Size/volume/mass measurements are typically lognormal, not normal.
        """
        import warnings

        units = self.empirical_data.units.lower()
        code = self.empirical_data.distribution_code

        # Check if units suggest size/volume/mass
        size_units = ["meter", "liter", "gram", "cell", "mole", "molarity"]
        is_size_data = any(unit in units for unit in size_units) and units != "dimensionless"

        # Check if using normal distribution (but not lognormal)
        uses_normal = ("normal(" in code or "randn" in code) and "lognormal" not in code

        if is_size_data and uses_normal:
            warnings.warn(
                f"Using normal distribution for size/volume/mass data (units: {units}). "
                f"These measurements are typically lognormally distributed. "
                f"Normal distributions can produce non-physical negative values requiring clipping. "
                f"Consider using lognormal distribution instead.",
                UserWarning,
            )

        return self

    @model_validator(mode="after")
    def validate_conversion_factors_documented(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Check that conversion factors are documented.

        Magic numbers in observable.code should be in observable.constants or study_interpretation/key_study_limitations.
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        import warnings

        # Universal constants to ignore (only truly fundamental values)
        UNIVERSAL_CONSTANTS = {
            # Basic integers
            0,
            1,
            2,
            3,
            4,
            # Mathematical constants
            np.pi,
            3.14159,
            3.141592653589793,
            np.e,
            2.71828,
            2.718281828459045,
            # Common fractions
            0.5,
            0.25,
            0.75,
            # Statistical percentiles (standard)
            2.5,
            25,
            50,
            75,
            97.5,
            0.025,
            0.25,
            0.5,
            0.75,
            0.975,
        }

        code = self.observable.code

        # Extract numeric literals from code
        try:
            tree = ast.parse(code)
            literals = []
            for node in ast.walk(tree):
                if isinstance(node, ast.Constant) and isinstance(node.value, (int, float)):
                    val = node.value
                    # Skip universal constants (with tolerance for floating point)
                    if not any(abs(val - c) < 1e-6 for c in UNIVERSAL_CONSTANTS):
                        literals.append(val)
        except SyntaxError:
            # If code doesn't parse, skip this check
            return self

        if literals:
            # Check if these appear in observable.constants or study_interpretation/key_study_limitations
            constant_values: list[float] = [c.value for c in self.observable.constants]

            # Check study_interpretation and key_study_limitations for documentation
            combined_text = self.study_interpretation + " " + " ".join(self.key_study_limitations)

            undocumented = []
            for lit in literals:
                # Check if value appears in constants (with tolerance)
                in_constants = any(abs(lit - v) < 1e-6 for v in constant_values)
                # Check if value appears in study_interpretation/key_study_limitations text
                in_text = str(lit) in combined_text or f"{lit:.0e}" in combined_text

                if not (in_constants or in_text):
                    undocumented.append(lit)

            if undocumented:
                warnings.warn(
                    f"observable.code contains numeric literals {undocumented} that may be conversion factors. "
                    f"Document these in observable.constants or explain in study_interpretation. "
                    f"Examples: cell sizes (µm), cellularity fractions, density values, geometric conversion factors.",
                    UserWarning,
                )

        return self

    @model_validator(mode="after")
    def validate_inputs_used(self) -> "CalibrationTarget":
        """Validator: Warn if inputs/assumptions are defined but not used in distribution_code."""
        # Extract all names accessed from 'inputs' dict
        used_input_names = set()

        # Pre-compute the set of candidate input names for string-constant lookup below.
        candidate_names = {inp.name for inp in self.empirical_data.inputs} | {
            a.name for a in self.empirical_data.assumptions
        }

        class InputAccessVisitor(ast.NodeVisitor):
            def visit_Subscript(self, node):
                # Look for inputs['name'] or inputs["name"]
                if isinstance(node.value, ast.Name) and node.value.id == "inputs":
                    if isinstance(node.slice, ast.Constant):
                        used_input_names.add(node.slice.value)
                self.generic_visit(node)

            def visit_Constant(self, node):
                # Accept string literals that match a defined input name — covers
                # patterns like `names = ['fc_01', ...]` / `inputs[name]` / f-strings,
                # where the static access via `inputs[<constant>]` is not directly visible.
                if isinstance(node.value, str) and node.value in candidate_names:
                    used_input_names.add(node.value)
                self.generic_visit(node)

        # Parse distribution_code to find variable references
        try:
            tree = ast.parse(self.empirical_data.distribution_code)
            visitor = InputAccessVisitor()
            visitor.visit(tree)
        except SyntaxError:
            # Already caught by validate_derivation_code
            pass

        # Inputs referenced via source_inputs of a derived_arithmetic input are counted as
        # used (they are consumed by the schema-level formula evaluator, not distribution_code).
        # Derived_arithmetic inputs themselves may be accessed via f-strings or
        # enumeration in distribution_code; we treat them as used if any
        # derived_arithmetic input has a name matching the access pattern.
        for inp in self.empirical_data.inputs:
            if inp.input_type == InputType.DERIVED_ARITHMETIC and inp.source_inputs:
                used_input_names.update(inp.source_inputs)

        # Check which defined inputs/assumptions are not used in distribution_code
        # Both EstimateInputs and ModelingAssumptions are merged into the inputs dict
        defined_inputs = {inp.name for inp in self.empirical_data.inputs}
        defined_assumptions = {a.name for a in self.empirical_data.assumptions}
        all_defined = defined_inputs | defined_assumptions
        unused = all_defined - used_input_names

        if unused:
            # This is a warning, not an error - could be intentional
            import warnings

            warnings.warn(
                f"The following inputs/assumptions are defined but not used in distribution_code: {sorted(unused)}. "
                f"If these are not needed, consider removing them.",
                UserWarning,
            )

        return self

    @model_validator(mode="after")
    def validate_no_hardcoded_constants_in_observable_code(self) -> "CalibrationTarget":
        """
        Validator (ERROR): Flag hardcoded numbers with units in observable.code.

        Uses AST-based detection to find numeric constants multiplied by ureg units.

        All numeric constants with units must be declared in observable.constants
        and accessed via the constants dict. This prevents arbitrary magic numbers
        and forces explicit documentation of biological assumptions.

        Allowed inline numbers:
        - Small integers (0-5)
        - Common fractions (0.5, 0.25, 0.75)
        - Statistical percentiles (2.5, 25, 50, 75, 97.5)
        - Common conversion factors (100, 1000)
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        from maple.core.calibration.code_validator import find_hardcoded_constants

        violations = find_hardcoded_constants(self.observable.code)

        if violations:
            violation_strs = [f"  • {v[0]} in '{v[3]}' (line {v[1]})" for v in violations[:5]]
            raise HardcodedConstantError(
                "Hardcoded numeric constants with units found in observable.code:\n"
                + "\n".join(violation_strs)
                + (f"\n  ... and {len(violations) - 5} more" if len(violations) > 5 else "")
                + "\n\nAll numeric constants with units must be declared in observable.constants "
                + "and accessed via the constants dict.\n"
                + "Example fix:\n"
                + "  1. Add to observable.constants:\n"
                + "     - name: area_per_cancer_cell\n"
                + "       value: 2.27e-4\n"
                + "       units: mm**2/cell\n"
                + "       biological_basis: 'From reference DB pdac_cancer_cell_diameter (17 μm) → area = π×(8.5 μm)²'\n"
                + "       source_type: derived_from_reference_db\n"
                + "       reference_db_names: [pdac_cancer_cell_diameter]\n"
                + "  2. Use in code: area_per_cell = constants['area_per_cancer_cell']"
            )

        return self

    @model_validator(mode="after")
    def validate_no_extreme_dimensionless_constants(self) -> "CalibrationTarget":
        """
        Validator (ERROR): Flag dimensionless constants with extreme magnitude.

        Dimensionless scaling factors > 10 or < 0.1 almost always indicate
        extraction errors rather than legitimate conversion factors. Legitimate
        conversions (cell area, volume, molecular weight) have physical units.

        This catches fudge factors like 'cd8_density_scale = 0.00283' or
        'cd8_fold_scaling_factor = 100' that mask model-data mismatches.
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        from maple.core.unit_registry import ureg

        for const in self.observable.constants:
            try:
                quantity = const.value * ureg(const.units)
                is_dimensionless = quantity.dimensionless
            except Exception:
                is_dimensionless = const.units.strip().lower() in ("dimensionless", "")

            if (
                is_dimensionless
                and const.value != 0
                and (abs(const.value) > 10 or abs(const.value) < 0.1)
            ):
                raise ScaleMismatchError(
                    f"Observable constant '{const.name}' has extreme dimensionless "
                    f"value {const.value}.\n"
                    f"Dimensionless scaling factors > 10 or < 0.1 almost always "
                    f"indicate an extraction error (wrong statistic type, wrong "
                    f"units, cross-arm confusion, or wrong denominator in a ratio).\n\n"
                    f"If this is a legitimate conversion factor, it should have "
                    f"physical units (e.g., mm**2/cell, not dimensionless).\n"
                    f"Re-examine the data extraction before adding scaling factors."
                )

        return self

    @model_validator(mode="after")
    def warn_extreme_constant_magnitude(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Flag constants with very large or very small values.

        Constants with |log10(value)| > 4 (i.e., value > 10000 or value < 0.0001)
        warrant scrutiny even when they have physical units, as they may indicate
        unit mismatches or conversion errors.
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        import math
        import warnings

        for const in self.observable.constants:
            if const.value == 0:
                continue
            log_magnitude = abs(math.log10(abs(const.value)))
            if log_magnitude > 4:
                warnings.warn(
                    f"Observable constant '{const.name}' has value {const.value} "
                    f"(magnitude ~10^{log_magnitude:.0f}). Constants with extreme "
                    f"magnitude often indicate unit mismatches or conversion errors. "
                    f"Verify the biological_basis is correct and the value uses "
                    f"appropriate units.",
                    UserWarning,
                )

        return self

    @model_validator(mode="after")
    def validate_species_completeness(self) -> "CalibrationTarget":
        """
        Validator (WARNING): Check for potentially missing related species.

        When measuring "total" quantities (e.g., total CD8 via pan-CD8 antibody),
        warns if related species that should be summed are not included.
        """
        # Skip for IsolatedSystemTarget (uses submodel.observable instead)
        if type(self).__name__ == "IsolatedSystemTarget":
            return self

        import warnings

        # Related species pairs - if one is used for a "total" measurement,
        # the other should likely be included too
        RELATED_SPECIES = {
            "V_T.CD8": ["V_T.CD8_exh"],
            "V_T.CD8_exh": ["V_T.CD8"],
            "V_T.Th": ["V_T.Treg"],
            "V_T.Treg": ["V_T.Th"],
            "V_T.M1": ["V_T.M2"],
            "V_T.M2": ["V_T.M1"],
        }

        # Keywords suggesting a "total" measurement
        TOTAL_KEYWORDS = ["total", "all", "overall", "pan-", "combined"]

        interpretation_lower = self.study_interpretation.lower()
        uses_total = any(kw in interpretation_lower for kw in TOTAL_KEYWORDS)

        if not uses_total:
            return self

        used_species = set(self.observable.species)
        for species in self.observable.species:
            related = RELATED_SPECIES.get(species, [])
            missing = [r for r in related if r not in used_species]
            if missing:
                warnings.warn(
                    f"Species completeness: '{species}' used for 'total' measurement "
                    f"but related species {missing} not included.\n"
                    f"If measuring total CD8+ via pan-CD8 antibody, include both CD8 and CD8_exh.",
                    UserWarning,
                )

        return self

    @model_validator(mode="after")
    def validate_no_control_characters(self) -> "CalibrationTarget":
        """
        Validator: Check for control characters in all string fields.

        Control characters (especially in titles, descriptions, snippets) can cause
        YAML parsing failures and other issues. This validator detects them early.

        Checks for:
        - ASCII control characters (0x00-0x1F) except tab (0x09), LF (0x0A), CR (0x0D)
        - DEL character (0x7F)
        - Unicode control characters (U+0080-U+009F)
        """
        import re

        # Pattern to match control characters (excluding valid whitespace)
        # \x00-\x08: NUL through BACKSPACE
        # \x0B-\x0C: VERTICAL TAB, FORM FEED
        # \x0E-\x1F: SHIFT OUT through UNIT SEPARATOR
        # \x7F: DEL
        # \x80-\x9F: Unicode control characters
        control_char_pattern = re.compile(r"[\x00-\x08\x0B-\x0C\x0E-\x1F\x7F\x80-\x9F]")

        def check_string(value: str, field_path: str) -> None:
            """Check a string for control characters."""
            match = control_char_pattern.search(value)
            if match:
                char_code = ord(match.group(0))
                char_hex = f"0x{char_code:02X}"
                # Get position in string
                pos = match.start()
                # Get context around the control character
                context_start = max(0, pos - 20)
                context_end = min(len(value), pos + 20)
                context = value[context_start:context_end]
                # Replace control char with placeholder for display
                context_display = context.replace(match.group(0), f"[{char_hex}]")

                raise ControlCharacterError(
                    f"Control character {char_hex} found in field '{field_path}' at position {pos}.\n"
                    f"Context: ...{context_display}...\n"
                    f"Control characters cause YAML parsing failures and must be removed.\n"
                    f"Common causes: copying from PDFs, word processors, or web pages with special formatting.\n"
                    f"Fix: Re-type the text or use a plain text editor to remove invisible characters."
                )

        def walk_and_check(obj, path: str = "") -> None:
            """Recursively walk through the object and check all strings."""
            if isinstance(obj, str):
                check_string(obj, path)
            elif isinstance(obj, dict):
                for key, value in obj.items():
                    walk_and_check(value, f"{path}.{key}" if path else key)
            elif isinstance(obj, list):
                for i, item in enumerate(obj):
                    walk_and_check(item, f"{path}[{i}]")
            elif hasattr(obj, "model_dump"):
                # Pydantic model - convert to dict and recurse
                walk_and_check(obj.model_dump(), path)

        # Check all fields in the model
        walk_and_check(self.model_dump(), "CalibrationTarget")

        return self
